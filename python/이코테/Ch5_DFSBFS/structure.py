# stack and queue

from collections import deque
stack = list()
stack.append(5)
stack.append(2)
stack.append(3)
stack.append(7)
stack.pop()
stack.append(1)
stack.append(4)
stack.pop()

print(f"stack: {stack}")


queue = deque()
queue.append(5)
queue.append(2)
queue.append(3)
queue.append(7)
queue.popleft()
queue.append(1)
queue.append(4)
queue.popleft()

print(f"queue: {queue}")
print(f"popleft: {queue.popleft()}")
